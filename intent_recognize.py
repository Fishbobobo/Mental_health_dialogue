def get_intent_result(str):
    if "一" in str or "用户" in str or "继续" in str:
        # print("意图1-围绕用户话题")
        return 1
    if "二" in str or "症状" in str or "选择" in str:
        # print("意图0-进行诊断询问")
        return 0
    return 1

def instruction_and_prompt_intent_judge(dialogue_history):
    instruction = """你是一个对话判断助手，任务是基于用户最近的对话历史来判断当前合适的对话策略。请通过分析用户的最后一句话，判断用户的意图，以便选择适当的策略。
    ========
具体规则：
1. **选择策略一：围绕用户话题进行对话**。当用户的最后一句话中包含以下任意一种情况，请选择策略一：
   - **引入新话题**：用户带来了与当前对话不同的新内容（如谈论新的学校事件、生活烦恼、未来计划等）。
   - **提出新问题**：用户提出了新的问题或寻求建议，明确希望得到更多信息或帮助。
   - **表达情绪**：用户表达了明显的情绪反应，例如高兴、愤怒、焦虑、失落等，这表明用户需要情绪上的理解和回应。
2. **选择策略二：选择症状话题进行对话**。当用户的最后一句话只是回应或跟随先前的对话内容，未引入新话题、未提出新问题、也没有明显的情绪表达时，选择策略二。例如，用户只是简单地附和、表示理解，或回应但未表露新意图。
==========
判断步骤：
1. **分析用户的最后一句话**，判断是否有新话题、新问题或情绪表达。注意：情绪表达可以通过用户的语气或用词间接体现，比如“最近总是感觉有点无聊”可能是对失落或焦虑的暗示。
2. **根据分析结果**，选择合适的策略，仅返回策略编号。

### 重要提示
- **新话题**：指的是任何与当前讨论内容明显不同的新信息或新想法。
- **新问题**：通常是用户期待帮助的问题，可能以疑问句或求助的语气呈现。
- **情绪表达**：包括直接描述情绪（如“我很高兴”），也包括间接的情绪流露（如“最近没什么干劲”），需要你根据语境来判断。
- **使用打招呼语言**：用户说出“你好”、“早安”、“下午好”等话语，通常是为了开启对话。

请严格选择策略一或策略二，不要返回其他内容。

**示例**：
- 对话历史：User: 这周事情好多，搞得我好累。
  - 策略判断：策略一（用户表达情绪）
- 对话历史：User: 哦，这样啊，我明白了。
  - 策略判断：策略二（用户只是回应，没有情绪或新话题）

    """

    prompt = f"""==========
对话历史如下：
{dialogue_history}
判断和输出标准如下，请仅输出相应内容：
1. 如果用户在最后一句话中引入新话题、提出新问题或表达情绪，请输出“策略一：继续用户话题”。
2. 如果用户没有引入新话题或新问题，也未表达情绪，而只是回应之前的对话内容，请输出“策略二：选择症状话题”。
==========
"""

    return instruction, prompt


# 进行意图识别
def intent_judge(dialogue_history, key):
    instruction, prompt = instruction_and_prompt_intent_judge(dialogue_history)
    intent = get_response_openai3(instruction, prompt, key)
    # print(intent)
    intent = get_intent_result(intent)
    # print(intent)
    return intent